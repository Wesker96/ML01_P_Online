# Шаг 1: Определение исходных данных

# Вероятность, что человек болен (P(bolen))
P_bolen = 0.01  # 1% больных в популяции

# Вероятность, что человек здоров (P(zdorov))
P_zdorov = 0.99  # Остальные 99% здоровы

# Вероятность положительного теста при наличии болезни (P(poloj|bolen))
P_poloj_bolen = 0.98  # Тест правильно выявляет болезнь в 98% случаев

# Вероятность положительного теста при отсутствии болезни (P(poloj|zdorov))
P_poloj_zdorov = 0.02  # Тест дает ложноположительный результат в 2% случаев

# Шаг 2: Вычисление общей вероятности положительного теста (P(poloj))

# Формула для общей вероятности положительного теста:
# P(poloj) = P(poloj|bolen) * P(bolen) + P(poloj|zdorov) * P(zdorov)
# Где:
# P(poloj|bolen) - Вероятность положительного теста при наличии болезни
# P(bolen) - Вероятность, что человек болен
# P(poloj|zdorov) - Вероятность положительного теста при отсутствии болезни
# P(zdorov) - Вероятность, что человек здоров

P_poloj = P_poloj_bolen * P_bolen + P_poloj_zdorov * P_zdorov

# Пояснение:
# Общая вероятность положительного теста включает оба случая:
# 1. Когда человек действительно болен и тест показывает положительный результат.
# 2. Когда человек здоров, но тест ошибочно показывает положительный результат.
# Мы суммируем эти вероятности.

print(f"Общая вероятность положительного теста P(poloj): {P_poloj:.4f}")

# Шаг 3: Вычисление вероятности того, что человек болен при положительном тесте (P(bolen|poloj))

# Формула для вычисления вероятности того, что человек болен при положительном тесте:
# P(bolen|poloj) = (P(poloj|bolen) * P(bolen)) / P(poloj)
# Где:
# P(poloj|bolen) - Вероятность положительного теста при наличии болезни
# P(bolen) - Вероятность, что человек болен
# P(poloj) - Общая вероятность положительного теста

P_bolen_given_poloj = (P_poloj_bolen * P_bolen) / P_poloj

# Пояснение:
# Используем теорему Байеса для вычисления вероятности того, что человек болен при условии,
# что тест дал положительный результат. Теорема Байеса позволяет пересчитать начальную вероятность,
# учитывая дополнительную информацию (в данном случае, положительный тест).

print(f"Вероятность того, что человек болен при положительном тесте P(bolen|poloj): {P_bolen_given_poloj:.4f}")
